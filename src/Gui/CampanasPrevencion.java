    private void search_buttonMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_search_buttonMousePressed
        DefaultTableModel model = (DefaultTableModel) registers_table.getModel();
        int rows = model.getRowCount();
        for (int i = rows - 1; i >= 0; i--) {
            model.removeRow(i);
        }
        Campana_Prevencion campana = new Campana_Prevencion();
        int cod = Integer.parseInt(JOptionPane.showInputDialog(null, "Inserte código de la Campaña", ""));
        campana = campana_control.consultarCampana(cod);
        if(campana != null){
            String nombre = campana.getNomCampana();
            String objetivo = campana.getObjetCampana();
            String medico = persona_control.consultarPersona(campana.getMedicoEncargado()).getNombre();
            String fecha = campana.getFechaCampana();
            
            model.addRow(new Object[]{cod, nombre, objetivo, medico, fecha});  
        }
        else
            JOptionPane.showMessageDialog(null, "Error. \n Verifique el número de Identidad del Paciente.");
    }//GEN-LAST:event_search_buttonMousePressed


    private void addCombos(){
        Vector <String> medicos = new Vector<String>();
        medicos = persona_control.listarMedicos();
        medicoCampana_box.removeAllItems();
        for(int i = 0; i < medicos.size(); i++){
            String m = medicos.get(i);
            medicoCampana_box.addItem(m);
        }
        
        Vector <Campana_Prevencion> campana = new Vector<Campana_Prevencion>();
        campana = campana_control.listarCampanas();
        campana_box.removeAllItems();
        for(int i = 0; i < campana.size(); i++){
            String m = campana.get(i).getNomCampana();
            campana_box.addItem(m);
        }
    }


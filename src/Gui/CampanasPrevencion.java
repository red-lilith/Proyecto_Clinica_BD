    private void filter_button1MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_filter_button1MousePressed
        DefaultTableModel model = (DefaultTableModel) registers_table1.getModel();
        int rows = model.getRowCount();
        for (int i = rows - 1; i >= 0; i--) {
            model.removeRow(i);
        }
        registers_table1.setModel(model);
        Vector <Asistencia_Campana> asistencias = new Vector <Asistencia_Campana>();
        String[] campanas = new String[campana_control.listarCampanas().size()];
        for(int i=0; i< campanas.length; i++){
            campanas[i] = campana_control.listarCampanas().get(i).getNomCampana();
        }
        String filtro = (String) JOptionPane.showInputDialog(null, "Seleccione una Campaña:", "FILTRAR", JOptionPane.DEFAULT_OPTION,null,campanas, campanas[0]);
        Campana_Prevencion campana = new Campana_Prevencion();
        campana = campana_control.consultarxNombre(filtro);
        int codCampana = campana.getCodCampana();
        asistencias = asistencia_control.filtrarAsistencias(codCampana);
        if(!asistencias.isEmpty()){
            for(int i = 0; i< asistencias.size(); i++){
                int id_pac = asistencias.get(i).getPaciente();
                String nom_pac = persona_control.consultarPersona(id_pac).getNombre();
                String nom_cam = campana_control.consultarCampana(codCampana).getNomCampana();
                model.addRow(new Object[]{id_pac, nom_pac, nom_cam}); 
            }     
        }
        else
            JOptionPane.showMessageDialog(null, "La Campaña no existe O no tiene asistentes todavía.");
    }//GEN-LAST:event_filter_button1MousePressed

